window.fnSetLanguage = function(sLang) {
  localStorage.setItem("vikingLang", sLang);
  fnTranslatePage();
  if (window.fnInitializeStory && window.oChapterData) {
    // Re-render current node to update text dynamically
    const eStoryText = document.getElementById("idStoryText");
    const eChoiceContainer = document.getElementById("idChoiceContainer");
    if (window.sCurrentNodeId && eStoryText && eChoiceContainer) {
      window.fnRenderNode(window.sCurrentNodeId, eStoryText, eChoiceContainer);
    }
  }
};

window.fnGetLanguage = function() {
  return localStorage.getItem("vikingLang") || "FR"; // Default to FR
};

window.fnTranslatePage = function() {
  const sLang = window.fnGetLanguage();
  const aElements = document.querySelectorAll("[data-i18n]");
  
  aElements.forEach(eEl => {
    const sKey = eEl.getAttribute("data-i18n");
    if (window.oTranslations[sKey] && window.oTranslations[sKey][sLang]) {
      eEl.textContent = window.oTranslations[sKey][sLang];
    }
  });

  const eSelect = document.getElementById("idLangSelect");
  if (eSelect) {
    eSelect.value = sLang;
  }
};